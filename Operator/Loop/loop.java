package Operator.Loop;

public class loop {
    public static void main(String arg[]){
        int i;
        i=1; //initilization
        while(i<=10){ //condition
            System.out.println(i);
            i++; //increment
        }
        //(initialization ; condition; increment)
        for(int j=0; j<=10; j++){
            System.out.println(j);
        }

    }
}
